//Swap numbers
public class Swap_nums {

	public static void main(String[] args) {
		int num1 = 79;
		int num2 = 26;
		int temp;
		System.out.println("original numbers "+num1+" and "+num2);
		temp = num1;
		num1 = num2;
		num2 = temp;
		System.out.println("original numbers after swapping "+num1+" and "+num2);
		

	}

}
